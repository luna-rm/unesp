void printf_mat(int order[2], int mat[MAX][MAX], int l, int c){
    Sleep(100-((order[0]+order[1])*2));

    printf("     ");

    for(int j = 0; j < order[1]; j++){
        printf("%3d. ", j+1);
    }

    printf("\n");

    for (int i = 1; i <= order[0]; i++){
        Sleep(45-(order[0]+order[1]));
        for(int j = 0; j <= order[1]; j++){
            if(j == 0){
                printf("%3d. ", i);
            } else {
                if(l != -1 && c != -1){
                    if(i-1 == l && j-1 == c){
                        printf(" ___ ");
                    } else {
                        if (i-1 < l) {
                            printf("%4d ", mat[i-1][j-1]); 
                        } else if (i-1 == l){
                            if (j-1 < c){
                                printf("%4d ", mat[i-1][j-1]); 
                            } else {
                                printf("     ");
                            }
                        } else {
                            printf("     ");
                        }
                    } 
                } else {
                    printf("%4d ", mat[i-1][j-1]);
                }
            }
        }
        printf("\n");
    }
    printf("\n\n");
}

