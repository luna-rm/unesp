int read_input(){

    int aux = getch();

    if(aux <= 0 || aux == 224){
        aux = getch();

        if(aux == HELP){
            system("cls");

            rand_sleep(ANIM_TIME-100, ANIM_TIME+100);

            help_scr();

            return 4;
        }

        if(aux == UP){
            return 1;
        }

        if(aux == DOWN){
            return 2;
        }
    }

    if(aux == ESC){
        cred();
    }

    if(aux == ENTER){
        return 3;
    }

    return -1;
}

